# lists are used to store multiple items in a single variable.

total_cost = 615.0
cost_breakdown = [100.0, 200.0, 50.0, 60.0, 205.0]

print(total_cost)
print(cost_breakdown)

# we can use the type() function to check the type of a variable

print("Type of total_cost", type(total_cost))
print("Type of cost_breakdown", type(cost_breakdown))
print("Type of cost_breakdown[0]", type(cost_breakdown[0]))
